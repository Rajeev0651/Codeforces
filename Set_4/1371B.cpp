#include <bits/stdc++.h>
using namespace std;

#define l long
#define ll long long
#define FOR(i, a, b) for (auto i = a; i <= b; i++)
#define FOREV(i, a, b) for (auto i = a; i >= b; i--)

void solve()
{
  ll N, R, ans = 0;
  cin >> N >> R;
  if (N > R)
  {
    ans = (R * (R + 1)) / 2;
  }
  else
  {
    N--;
    ans = (N * (N + 1)) / 2;
    ans++;
  }
  cout << ans << "\n";
}

int main()
{
  ios_base::sync_with_stdio(0), cin.tie(0), cout.tie(0);
  int T;
  cin >> T;
  while (T--)
  {
    solve();
  }
  return 0;
}